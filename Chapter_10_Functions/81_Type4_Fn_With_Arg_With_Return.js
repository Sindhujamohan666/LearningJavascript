function sumofTwoNumbers(num1, num2){
    return num1 + num2;
}

let c=sumofTwoNumbers(5, 10); // Output: 15
console.log(c); // Output: 15 (because the function returns the sum of num1 and num2 we can print variable c)   

let d=sumofTwoNumbers(20, 30); // Output: 50
console.log(d); // Output: 50 (because the function returns the sum of num1 and num2 we can print variable d)